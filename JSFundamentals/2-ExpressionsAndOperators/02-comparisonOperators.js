//Equality
//two looks for value
"3" == 3; //true
//three looks for value and data type
"3" === 3; //false

//Inequality
2 != 1; //true
"3" !== 3; //true

//Greater than less than
3 > 1; //true
3 < 2; //false
//Greater than or equal to
3 >= 3; //true
4 < 4; //false

// And
2 > 1 && 2 < 5; //true

// Or
2 > 4 || 2 < 1; //true